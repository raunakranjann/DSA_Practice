// Question 2 : WCPP Program to calculate the AVERAGE of 3 Numbers. 
// Input : a, b, c 
// Output : (a+b+c)/3

#include <iostream>
using namespace std;

    
int main()
{
    int a, b, c;
    cout<<"Enter the values of a, b, and c"<<endl;
    cin>>a>>b>>c;
    cout<<"The average of "<<a<<", "<<b<<", and "<<c<<" is "<<(a+b+c)/3<<endl;
    return 0;
}